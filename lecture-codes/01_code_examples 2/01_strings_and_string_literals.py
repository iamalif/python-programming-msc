
# Strings and string literals

# A string is a sequence of characters enclosed in single or double quotes.

# Examples of strings
string1 = "Hello, World!" # A string enclosed in double quotes
string2 = 'Python is fun!' # A string enclosed in single quotes

# Display the strings
print(string1)
print(string2)

# String literals are special characters that have a specific meaning in a
# string.

# Examples of string literals
string_literal1 = "Hello, \"World!\"" # The double quote is escaped using a backslash
string_literal2 = 'Python\'s syntax is easy to learn.' # The single quote is escaped using a backslash
string_literal3 = """String "literals" can also be 'enclosed' in triple
                  quotes. The can span multiple lines."""
                  
                  
# Display the strings and string literals
print(string_literal1)
print(string_literal2)
print(string_literal3)