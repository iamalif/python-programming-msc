
# Variables

# Variables are used to store data in a program.
# They are used to store values that can be used later in the program.
# Variables can store different types of data, such as numbers, strings, and lists.

# Variables are created by assigning a value to a name.
#
# The name of the variable is called an identifier.
# The value of the variable is called the data.
#
# The assignment operator is used to assign a value to a variable.

# Examples of variables
x = 10 # An integer variable
y = 20 # Another integer variable
name = "Alice" # A string variable
pi = 3.14159 # A floating-point variable

# Display the variables
print(x)
print(y)
print(name)
print(pi)

# Variables can be used in expressions and calculations.
# The value of a variable can be updated by assigning a new value to it.

# Update the value of x
x = 15

# There are some rules for naming variables:
# 
# 1. Variable names can contain letters, numbers, and underscores.
# 2. Variable names cannot start with a number.
# 3. Variable names are case-sensitive.
# 4. Variable names should be descriptive and meaningful.

# Examples of bad variable names
a = 10 # Not descriptive
# 1x = 20 # Cannot start with a number
# X = 30 # Case-sensitive
# x = 40 # Variable name already used
# if = 50 # Cannot use reserved keywords as variable names

# Examples of good variable names
hours_worked = 10 # Descriptive
_x = 20 # Starts with an underscore, ugly but valid
x1 = 30 # Contains a number, but not at the beginning



