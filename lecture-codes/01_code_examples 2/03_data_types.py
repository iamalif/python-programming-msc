
# Data types in Python

# Python has several built-in data types that are used to store different types of data.
# The most common data types are:

# 1. Integer: A whole number, positive or negative, without decimals.
# 2. Float: A number with a decimal point.
# 3. String: A sequence of characters enclosed in single or double quotes.
# 4. Boolean: A value that can be either True or False.
# 5. List: A collection of items that are ordered and changeable.
# 6. Tuple: A collection of items that are ordered and unchangeable.
# 
# And many more...

# Examples of data types

# Integer
x = 10

# Float
y = 20.5

# String
name = "Alice"

# Boolean
is_student = True

# List
fruits = ["apple", "banana", "cherry"]

# Tuple
colors = ("red", "green", "blue")